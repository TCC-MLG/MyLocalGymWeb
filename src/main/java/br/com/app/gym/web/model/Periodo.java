package br.com.app.gym.web.model;

/**
 * @author Luciano
 */
public class Periodo {
    
    private String mensal;
    private String semestral;
    private String anual;

    public String getMensal() {
        return mensal;
    }

    public void setMensal(String mensal) {
        this.mensal = mensal;
    }

    public String getSemestral() {
        return semestral;
    }

    public void setSemestral(String semestral) {
        this.semestral = semestral;
    }

    public String getAnual() {
        return anual;
    }

    public void setAnual(String anual) {
        this.anual = anual;
    }
    
}
